const root = document.documentElement;
const savedTheme = localStorage.getItem("routina-theme");
const savedLang = localStorage.getItem("routina-lang");
const preferredDark = window.matchMedia("(prefers-color-scheme: dark)").matches;
let activeLang = savedLang || (navigator.language.toLowerCase().startsWith("th") ? "th" : "en");

root.dataset.theme = savedTheme || (preferredDark ? "dark" : "light");

function icon(name) {
  const icons = {
    sun: '<svg aria-hidden="true" width="19" height="19" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><circle cx="12" cy="12" r="4"/><path d="M12 2v2M12 20v2M4.93 4.93l1.41 1.41M17.66 17.66l1.41 1.41M2 12h2M20 12h2M4.93 19.07l1.41-1.41M17.66 6.34l1.41-1.41"/></svg>',
    moon: '<svg aria-hidden="true" width="19" height="19" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><path d="M20.99 12.79A9 9 0 1 1 11.21 3a7 7 0 0 0 9.78 9.79Z"/></svg>'
  };
  return icons[name];
}

function applyLang(lang) {
  activeLang = lang;
  localStorage.setItem("routina-lang", lang);
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (i18n[lang][key]) node.textContent = i18n[lang][key];
  });
  document.querySelectorAll("[data-i18n-aria]").forEach((node) => {
    const key = node.dataset.i18nAria;
    if (i18n[lang][key]) node.setAttribute("aria-label", i18n[lang][key]);
  });
}

function applyThemeIcon() {
  const button = document.querySelector("[data-theme-toggle]");
  if (!button) return;
  button.innerHTML = root.dataset.theme === "dark" ? icon("sun") : icon("moon");
}

document.addEventListener("DOMContentLoaded", () => {
  applyLang(activeLang);
  applyThemeIcon();

  document.querySelectorAll("[data-lang-toggle]").forEach((button) => {
    button.addEventListener("click", () => applyLang(activeLang === "en" ? "th" : "en"));
  });

  document.querySelectorAll("[data-theme-toggle]").forEach((button) => {
    button.addEventListener("click", () => {
      root.dataset.theme = root.dataset.theme === "dark" ? "light" : "dark";
      localStorage.setItem("routina-theme", root.dataset.theme);
      applyThemeIcon();
    });
  });
});
